import os

def get_template_for_category(category: str) -> str:
    """
    주어진 카테고리에 맞는 번역 양식 텍스트를 반환합니다.
    정확히 매칭되는 키가 없으면 부분 매칭을 시도하고,
    그래도 없으면 DEFAULT_TEMPLATE을 반환합니다.
    """
    template = DEFAULT_TEMPLATE

    if category:
        # 1) 정확히 일치
        if category in CATEGORY_TEMPLATES:
            template = CATEGORY_TEMPLATES[category]
        else:
            # 2) 부분 매칭 (예: "가방" → "가방/지갑" 매칭)
            matched = False
            for key, temp in CATEGORY_TEMPLATES.items():
                if category in key or key in category:
                    template = temp
                    matched = True
                    break
            
            # 3) 키워드 기반 폴백
            if not matched:
                cat_lower = category.lower()
                if "신발" in cat_lower or "슈즈" in cat_lower:
                    template = CATEGORY_TEMPLATES["신발"]
                elif "가방" in cat_lower or "지갑" in cat_lower or "백" in cat_lower:
                    template = CATEGORY_TEMPLATES["가방/지갑"]
                elif "남성" in cat_lower:
                    template = CATEGORY_TEMPLATES["남성의류"]
                elif "여성" in cat_lower:
                    template = CATEGORY_TEMPLATES["여성의류"]
                elif "시계" in cat_lower or "잡화" in cat_lower or "악세" in cat_lower:
                    template = CATEGORY_TEMPLATES["시계/잡화"]

    # 4) [AI 스타일 딥러닝 파이프라인] 카테고리/공통/밴드 기준 지침 병합
    try:
        try:
            from backend.style_context import load_style_blocks
        except ImportError:
            from style_context import load_style_blocks

        style_blocks = load_style_blocks(category, include_band=True)
        if style_blocks:
            template += "\n\n" + "═"*40 + "\n"
            template += "🚨 [사장님 맞춤형 AI 스타일 딥러닝 지침 (최우선 적용)] 🚨\n"
            template += "아래의 지침은 사용자의 과거 포스팅을 딥러닝하여 추출한 고유의 톤앤매너, 레이아웃, 기호 사용법입니다.\n"
            template += "이 지침을 위의 카테고리별 양식보다 더 우선적으로, 완벽하게 모방하여 적용하세요.\n\n"
            template += "\n\n".join(f"[{label}]\n{text}" for label, text in style_blocks)
    except Exception as e:
        print(f"스타일 프롬프트 로딩 실패: {e}")

    return template

def replace_emoji_to_basic(text: str) -> str:
    """이모지/특수문자들을 기본 기호로 치환"""
    if not text:
        return ""

    # ✅ 이모지 뒤에 붙는 보이지 않는 문자(️) / 피부톤 제거 → 치환 누락 방지
    text = text.replace("\ufe0f", "")  # variation selector
    text = re.sub(r"[\U0001F3FB-\U0001F3FF]", "", text)  # skin tone modifiers
    text = text.replace("\u200d", "")  # ZWJ (이모지 결합용)

    for src, dst in EMOJI_TO_BASIC:
        text = text.replace(src, dst)
    return text


def _is_korean(c: str) -> bool:
    o = ord(c)
    return (0xAC00 <= o <= 0xD7A3) or (0x3131 <= o <= 0x318E)  # 한글 음절/자모


def _is_eng_digit(c: str) -> bool:
    return c.isascii() and c.isalnum()


def _filter_text_keep_allowed(text: str, allowed_specials: set) -> str:
    """태그 밖 텍스트만: (한글/영문/숫자/공백 + 허용특수문자)만 남김"""
    if not text:
        return ""

    text = replace_emoji_to_basic(text)

    out = []
    for c in text:
        if c in ("\n", "\t", " "):
            out.append(c)
        elif _is_korean(c) or _is_eng_digit(c):
            out.append(c)
        elif c in allowed_specials:
            out.append(c)
        else:
            # 나머지는 전부 제거
            pass

    cleaned = "".join(out)

    # 공백/줄바꿈 정리
    cleaned = re.sub(r"[ \t]{2,}", " ", cleaned)
    cleaned = re.sub(r"\n{3,}", "\n\n", cleaned)

    return cleaned
